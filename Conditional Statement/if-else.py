# Conditional Statement

# Ask age from user 
# you can vote if 18 above

age = int(input("Enter your age = "))

if age >= 18 :
    print("You can vote")                         #In the beginning the space is called as indentation.
    print("You are allowed to vote")              #Indentation refers to the space at the beginning of a code line.

else :
    print("You cannot vote")                      #In the beginning the space is called as indentation.
    print("You are not allowed to vote")          #Indentation refers to the space at the beginning of a code line.