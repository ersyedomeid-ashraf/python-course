# Logical Operator 
# Result is always in boolean form (Yes/No) (True/False)

"""
Not reverse the result 
"""
physics = 86
chemistry = 15

print(physics > 33 and chemistry > 33)
print(physics > 33 or chemistry > 33)

print(physics > 33)
print(not physics > 33) 



english = 33
mathematic = 78
science = 98

print(english > 33 and mathematic > 33 and science > 33)
print(english > 33 or mathematic > 33 or science > 33)
print(not english > 33, not mathematic > 33, not science > 33)
