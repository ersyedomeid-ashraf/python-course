"""
Write a program to print 1 to 10. 
"""

for i in range (1, 10):
   print(i, end = " ")



"""
Write a program to print 1 to 20 ..... 1 3 5 7 9 ...... 19 
"""


for i in range(1, 21, 2) :       # 2 is the step size; it increases by 2 each time 
    print(i, end = " ")




"""
Write a program to print 10 to 0 
"""

for i in range(10,0,-1) :
    print(i, end = " ") 